import random
print(random.randint(5, 20))  # line 1: The smallest number: 5; The biggest number: 20.
print(random.randrange(3, 10, 2))  # line 2: The smallest number: 3; The biggest number: 9.
print(random.uniform(2.5, 5.5))  # line 3: The smallest number: 2.5; The biggest number: 5.5.

print(random.uniform(1, 100))